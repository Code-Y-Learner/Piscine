void	ft_advanced_sort_string_tab(char **tab, int (*cmp)(char *, char *));
int		word_cnt(char **tab);

int	word_cnt(char **tab)
{
	int	i;

	i = 0;
	while (tab[i])
		i++;
	return (i);
}

void	ft_advanced_sort_string_tab(char **tab, int (*cmp)(char *, char *))
{
	int		i;
	int		j;
	int		words;
	char	*tmp;

	words = word_cnt(tab);
	i = words - 1;
	while (i >= 0)
	{
		j = 0;
		while (j < i)
		{
			if (cmp(tab[j], tab[j + 1]) > 0)
			{
				tmp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = tmp;
			}
			j++;
		}
		i--;
	}
}
